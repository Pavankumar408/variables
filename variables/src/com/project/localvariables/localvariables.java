package com.project.localvariables;

public class localvariables {
	public void sum() {
		int a=10;
		int b=20;
		int sum=a+b;
		System.out.println(sum);
		
	}
	public void sub() {
		int a=20;
		int b=15;
		int sub=a-b;
		System.out.println(sub);
		
	}
	public void mul() {
		int a=20;
		int b=4;
		int mul=a*b;
		System.out.println(mul);
	}
	public void div() {
		int a=50;
		int b=5;
		int div=a/b;
		System.out.println(div);
		
	}

}
